import turtle

turtle.setup(650, 650)
turtle.pensize(5)        #画笔宽度
turtle.pencolor("red") #修改画笔颜色，也可以用这种方式turtle.pencolor(1,1,1)
turtle.speed(1)
turtle.penup()
turtle.forward(-100)
turtle.pendown()
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)

turtle.mainloop()